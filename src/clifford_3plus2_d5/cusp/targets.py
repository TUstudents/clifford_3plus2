@dataclass(frozen=True)
class NumericalSemigroup:
    """Numerical semigroup generated by positive coprime integers."""

    generators: tuple[int, ...]

    def __post_init__(self) -> None:
        """Validate generators."""

        if not self.generators:
            raise ValueError("need at least one generator")
        if any(generator <= 0 for generator in self.generators):
            raise ValueError("generators must be positive")
        common = self.generators[0]
        for generator in self.generators[1:]:
            common = gcd(common, generator)
        if common != 1 and len(self.generators) > 1:
            raise ValueError("multi-generator numerical semigroup must be coprime")

    def decompositions(self, value: int) -> tuple[tuple[int, ...], ...]:
        """Return all non-negative decompositions of ``value``."""

        if value < 0:
            return ()
        return tuple(
            decomposition
            for decomposition in self._decompositions_recursive(value, self.generators)
        )

    def _decompositions_recursive(
        self, value: int, generators: tuple[int, ...]
    ) -> tuple[tuple[int, ...], ...]:
        """Return decompositions recursively."""

        if len(generators) == 1:
            generator = generators[0]
            if value % generator == 0:
                return ((value // generator,),)
            return ()

        first, *rest = generators
        found: list[tuple[int, ...]] = []
        for count in range(value // first + 1):
            remainder = value - count * first
            for tail in self._decompositions_recursive(remainder, tuple(rest)):
                found.append((count, *tail))
        return tuple(found)

    def contains(self, value: int) -> bool:
        """Return whether ``value`` lies in the semigroup."""

        return bool(self.decompositions(value))

    def first_values(self, count: int) -> tuple[int, ...]:
        """Return the first ``count`` semigroup values."""

        if count <= 0:
            raise ValueError("count must be positive")
        values: list[int] = []
        value = 0
        while len(values) < count:
            if self.contains(value):
                values.append(value)
            value += 1
        return tuple(values)

    def values_through(self, bound: int) -> tuple[int, ...]:
        """Return semigroup values through ``bound``."""

        if bound < 0:
            raise ValueError("bound must be non-negative")
        return tuple(value for value in range(bound + 1) if self.contains(value))

    def gaps_through(self, bound: int) -> tuple[int, ...]:
        """Return missing values through ``bound``."""

        if bound < 0:
            raise ValueError("bound must be non-negative")
        return tuple(value for value in range(bound + 1) if not self.contains(value))

    def conductor(self, search_bound: int = 128) -> int:
        """Return the numerical-semigroup conductor.

        The conductor is the smallest integer ``c`` such that every integer
        ``n >= c`` lies in the semigroup.  For the cusp semigroup ``<2,3>`` it
        is ``2``, because ``1`` is the only gap.
        """

        if search_bound < 0:
            raise ValueError("search_bound must be non-negative")
        for candidate in range(search_bound + 1):
            if all(self.contains(value) for value in range(candidate, search_bound + 1)):
                return candidate
        raise ValueError("increase search_bound to certify the conductor")

    def frobenius_number(self, search_bound: int = 128) -> int | None:
        """Return largest certified gap, or ``None`` if there is no gap."""

        gaps = self.gaps_through(search_bound)
        if not gaps:
            return None
        conductor = self.conductor(search_bound)
        if gaps[-1] == conductor - 1:
            return gaps[-1]
        raise ValueError("increase search_bound to certify the Frobenius number")


@dataclass(frozen=True)
class BCCEdgeBranch:
    """One BCC edge branch in the two-normal q-clock reduction."""

    sigma_1: int
    sigma_2: int

    def __post_init__(self) -> None:
        """Validate BCC signs."""

        if self.sigma_1 not in (-1, 1) or self.sigma_2 not in (-1, 1):
            raise ValueError("BCC edge signs must be +/-1")

    @property
    def delta_q(self) -> int:
        """Return ``Delta q = sigma_1 - sigma_2``."""

        return self.sigma_1 - self.sigma_2

    @property
    def is_same_normal(self) -> bool:
        """Return whether this branch stays in the q=0 same-normal scar."""

        return self.delta_q == 0


@dataclass(frozen=True)
class BCCEdgeQClock:
    """Finite q-clock certificate for the BCC edge scar."""

    branches: tuple[BCCEdgeBranch, ...] = (
        BCCEdgeBranch(1, 1),
        BCCEdgeBranch(1, -1),
        BCCEdgeBranch(-1, 1),
        BCCEdgeBranch(-1, -1),
    )

    @property
    def same_normal_branches(self) -> tuple[BCCEdgeBranch, ...]:
        """Return q-preserving same-normal branches."""

        return tuple(branch for branch in self.branches if branch.is_same_normal)

    @property
    def mixed_normal_branches(self) -> tuple[BCCEdgeBranch, ...]:
        """Return q-changing leakage branches."""

        return tuple(branch for branch in self.branches if not branch.is_same_normal)

    @property
    def same_normal_count(self) -> int:
        """Return number of q=0 same-normal branches."""

        return len(self.same_normal_branches)

    @property
    def mixed_normal_count(self) -> int:
        """Return number of q-changing branches."""

        return len(self.mixed_normal_branches)

    @property
    def same_normal_delta_q_values(self) -> tuple[int, ...]:
        """Return q-deltas for same-normal branches."""

        return tuple(branch.delta_q for branch in self.same_normal_branches)

    @property
    def mixed_normal_delta_q_values(self) -> tuple[int, ...]:
        """Return q-deltas for mixed-normal branches."""

        return tuple(branch.delta_q for branch in self.mixed_normal_branches)

    @property
    def q_clock_pass(self) -> bool:
        """Return whether the BCC edge has the required q=0 branch split."""

        return (
            self.same_normal_count == 2
            and self.mixed_normal_count == 2
            and self.same_normal_delta_q_values == (0, 0)
            and sorted(self.mixed_normal_delta_q_values) == [-2, 2]
        )


@dataclass(frozen=True)
class RecirculationGraph:
    """Minimal return-order graph for Target A."""

    primitive_closure_lengths: tuple[int, ...]
    one_step_return_allowed: bool = False

    @property
    def semigroup(self) -> NumericalSemigroup:
        """Return the generated valuation semigroup."""

        generators = self.primitive_closure_lengths
        if self.one_step_return_allowed and 1 not in generators:
            generators = (1, *generators)
        return NumericalSemigroup(generators)

    @property
    def one_step_forbidden(self) -> bool:
        """Return whether valuation 1 is absent."""

        return not self.semigroup.contains(1)

    @property
    def primitive_closures(self) -> tuple[int, ...]:
        """Return primitive closure lengths."""

        return self.primitive_closure_lengths


def minimal_generators_from_values(values: tuple[int, ...]) -> tuple[int, ...]:
    """Return the minimal numerical-semigroup generators for sorted values."""

    generators: list[int] = []
    for value in sorted(value for value in values if value > 0):
        if not generators or not NumericalSemigroup(tuple(generators)).contains(value):
            generators.append(value)
    return tuple(generators)


@dataclass(frozen=True)
class FiniteRecirculationWalkGraph:
    """Finite directed graph whose closed walks model recirculation returns."""

    edges: tuple[tuple[str, str], ...]
    visible_node: str = "visible"

    @property
    def nodes(self) -> tuple[str, ...]:
        """Return graph nodes."""

        nodes = {self.visible_node}
        for source, target in self.edges:
            nodes.add(source)
            nodes.add(target)
        return tuple(sorted(nodes))

    def outgoing(self, node: str) -> tuple[str, ...]:
        """Return outgoing neighbors with multiplicity."""

        return tuple(target for source, target in self.edges if source == node)

    def closed_walk_count(self, length: int) -> int:
        """Return the number of closed walks of exact ``length`` from visible."""

        if length < 0:
            raise ValueError("length must be non-negative")
        positions = {self.visible_node: 1}
        for _ in range(length):
            next_positions: dict[str, int] = {}
            for node, count in positions.items():
                for target in self.outgoing(node):
                    next_positions[target] = next_positions.get(target, 0) + count
            positions = next_positions
        return positions.get(self.visible_node, 0)

    def closed_walk_counts_through(self, bound: int) -> tuple[int, ...]:
        """Return closed-walk counts for lengths ``0..bound``."""

        if bound < 0:
            raise ValueError("bound must be non-negative")
        return tuple(self.closed_walk_count(length) for length in range(bound + 1))

    def closed_walk_lengths_through(self, bound: int) -> tuple[int, ...]:
        """Return lengths through ``bound`` with nonzero closed walks."""

        return tuple(
            length
            for length, count in enumerate(self.closed_walk_counts_through(bound))
            if count > 0
        )

    def primitive_closed_walk_lengths_through(self, bound: int) -> tuple[int, ...]:
        """Return primitive generators inferred from closed-walk lengths."""

        return minimal_generators_from_values(self.closed_walk_lengths_through(bound))

    @property
    def one_step_forbidden(self) -> bool:
        """Return whether no one-step closed walk exists."""

        return self.closed_walk_count(1) == 0


