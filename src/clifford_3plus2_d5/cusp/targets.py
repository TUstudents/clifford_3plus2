@dataclass(frozen=True)
class CenterChargeRecirculationAutomaton:
    """Local center-charge recirculation automaton for one boundary source.

    One boundary tick advances the fundamental center charge by one unit.  The
    visible sheet can read out only the neutral charge state.  This turns a
    center order ``N`` into a primitive return length ``N`` without inserting a
    fixed path length by hand.
    """

    source: CenterClosureSource
    visible_node: str = "visible"

    def charge_after_ticks(self, ticks: int) -> int:
        """Return the center charge after ``ticks`` fundamental recirculations."""

        if ticks < 0:
            raise ValueError("ticks must be non-negative")
        return ticks % self.source.center_order

    def is_visible_return_allowed(self, ticks: int) -> bool:
        """Return whether ``ticks`` gives a nontrivial visible return."""

        return ticks > 0 and self.charge_after_ticks(ticks) == 0

    @property
    def one_step_forbidden(self) -> bool:
        """Return whether one local recirculation tick is center charged."""

        return not self.is_visible_return_allowed(1)

    @property
    def primitive_return_length(self) -> int:
        """Return the first allowed visible return length."""

        for ticks in range(1, self.source.center_order + 1):
            if self.is_visible_return_allowed(ticks):
                return ticks
        raise RuntimeError("unreachable for finite cyclic center")

    def charge_table(self, ticks_through: int) -> tuple[int, ...]:
        """Return center charges for ticks ``0..ticks_through``."""

        if ticks_through < 0:
            raise ValueError("ticks_through must be non-negative")
        return tuple(self.charge_after_ticks(ticks) for ticks in range(ticks_through + 1))

    def return_ticks_through(self, ticks_through: int) -> tuple[int, ...]:
        """Return allowed nontrivial visible return ticks through a bound."""

        if ticks_through < 0:
            raise ValueError("ticks_through must be non-negative")
        return tuple(
            ticks
            for ticks in range(1, ticks_through + 1)
            if self.is_visible_return_allowed(ticks)
        )

    def recirculation_edges(self) -> tuple[tuple[str, str], ...]:
        """Return the finite graph edges generated by local charge evolution."""

        edges: list[tuple[str, str]] = []
        for channel in range(self.source.channel_count):
            first = self._state_label(channel, charge=1)
            edges.append((self.visible_node, first))
            for charge in range(1, self.source.center_order):
                source = self._state_label(channel, charge)
                next_charge = (charge + 1) % self.source.center_order
                target = (
                    self.visible_node
                    if next_charge == 0
                    else self._state_label(channel, next_charge)
                )
                edges.append((source, target))
        return tuple(edges)

    def _state_label(self, channel: int, charge: int) -> str:
        """Return a deterministic hidden charge-state label."""

        return f"{self.source.name}_channel_{channel}_center_charge_{charge}"


