@dataclass(frozen=True)
class CoefficientPathMeasure:
    """Finite path-sum measure for one coefficient matrix."""

    paths: tuple[CenterHolonomyPath, ...]

    def coefficient_matrix(self) -> sp.Matrix:
        """Return ``c_ij=sum_gamma A_gamma Omega_gamma``."""

        matrix = sp.zeros(3, 3)
        for path in self.paths:
            matrix[path.row, path.col] += path.contribution()
        return sp.simplify(matrix)

    def center_power_matrix(self) -> tuple[tuple[int, int, int], ...]:
        """Return powers when each entry has one unit-amplitude path."""

        powers: list[list[int]] = [[0 for _ in range(3)] for _ in range(3)]
        counts: list[list[int]] = [[0 for _ in range(3)] for _ in range(3)]
        for path in self.paths:
            powers[path.row][path.col] = path.normalized_center_power
            counts[path.row][path.col] += 1
        if any(count != 1 for row in counts for count in row):
            raise ValueError("center_power_matrix requires exactly one path per entry")
        return tuple(tuple(row) for row in powers)


def path_measure_from_center_powers(
    phase_exponents: tuple[tuple[int, int, int], ...],
    amplitude: sp.Expr | tuple[tuple[sp.Expr, sp.Expr, sp.Expr], ...] = sp.Integer(1),
) -> CoefficientPathMeasure:
    """Return a one-path-per-entry measure from center powers."""

    amplitude_matrix = normalize_path_amplitudes(amplitude)
    return CoefficientPathMeasure(
        paths=tuple(
            CenterHolonomyPath(
                row=row,
                col=col,
                amplitude=amplitude_matrix[row][col],
                center_power=power,
            )
            for row, phase_row in enumerate(phase_exponents)
            for col, power in enumerate(phase_row)
        )
    )


def normalize_path_amplitudes(
    amplitude: sp.Expr | tuple[tuple[sp.Expr, sp.Expr, sp.Expr], ...],
) -> tuple[tuple[sp.Expr, sp.Expr, sp.Expr], ...]:
    """Return a 3x3 amplitude matrix from a scalar or entrywise amplitudes."""

    if isinstance(amplitude, sp.MatrixBase):
        if amplitude.shape != (3, 3):
            raise ValueError("entrywise amplitude matrix must be 3x3")
        return tuple(
            tuple(sp.sympify(amplitude[row, col]) for col in range(3))
            for row in range(3)
        )
    if isinstance(amplitude, (tuple, list)):
        if len(amplitude) != 3 or any(len(row) != 3 for row in amplitude):
            raise ValueError("entrywise amplitude matrix must be 3x3")
        return tuple(
            tuple(sp.sympify(amplitude[row][col]) for col in range(3))
            for row in range(3)
        )
    scalar = sp.sympify(amplitude)
    return tuple(tuple(scalar for _ in range(3)) for _ in range(3))


