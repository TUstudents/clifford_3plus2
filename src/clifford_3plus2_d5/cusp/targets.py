def exponent_matrix(
    left_charges: tuple[int, int, int], right_charges: tuple[int, int, int]
) -> tuple[tuple[int, int, int], ...]:
    """Return FN exponent matrix ``q_i+r_j``."""

    return tuple(tuple(left + right for right in right_charges) for left in left_charges)


def solve_right_charges_from_diagonal_targets(
    left_charges: tuple[int, int, int],
    diagonal_targets: tuple[int, int, int],
    max_charge: int = 8,
) -> tuple[tuple[int, int, int], ...]:
    """Solve ``left_i + right_i = target_i`` for nonnegative right charges."""

    solutions: list[tuple[int, int, int]] = []
    for first in range(max_charge + 1):
        for second in range(max_charge + 1):
            for third in range(max_charge + 1):
                candidate = (first, second, third)
                diagonal = tuple(left + right for left, right in zip(left_charges, candidate))
                if diagonal == diagonal_targets:
                    solutions.append(candidate)
    return tuple(solutions)


def subtract_conductor_floor(
    charges: tuple[int, int, int], conductor: int
) -> tuple[int, int, int]:
    """Return ``max(q_i-conductor,0)`` for a cusp-conductor floor."""

    return tuple(max(charge - conductor, 0) for charge in charges)


def oriented_double_up_right_charges(
    left_charges: tuple[int, int, int],
    down_right_charges: tuple[int, int, int],
) -> tuple[int, int, int]:
    """Return up right charges from doubled down diagonal exponents."""

    return oriented_lift_up_right_charges(
        left_charges, down_right_charges, lift_factor=2
    )


def oriented_lift_up_right_charges(
    left_charges: tuple[int, int, int],
    down_right_charges: tuple[int, int, int],
    lift_factor: int,
) -> tuple[int, int, int]:
    """Return up right charges from an oriented lift of the down diagonal."""

    down_diagonal = tuple(
        left + right for left, right in zip(left_charges, down_right_charges)
    )
    up_diagonal = tuple(lift_factor * exponent for exponent in down_diagonal)
    return tuple(up - left for up, left in zip(up_diagonal, left_charges))


def diagonal_exponents(
    left_charges: tuple[int, int, int], right_charges: tuple[int, int, int]
) -> tuple[int, int, int]:
    """Return diagonal FN exponents ``q_i+r_i``."""

    return tuple(left + right for left, right in zip(left_charges, right_charges))


def yukawa_hypercharge_sum(
    q_left_bar: sp.Expr, higgs: sp.Expr, right: sp.Expr
) -> sp.Expr:
    """Return the hypercharge sum for ``bar(Q_L) H q_R``."""

    return sp.simplify(q_left_bar + higgs + right)


@dataclass(frozen=True)
class CuspBoundaryModuleRule:
    """Finite right-charge rule for a cusp boundary module.

    The rule is intentionally explicit: the down module is the left valuation
    module quotiented by the cusp conductor floor, while the up module is an
    oriented lift of the resulting down diagonal exponents.  The microscopic
    boundary dynamics selecting this module rule remains an open theorem gate.
    """

    name: str
    left_charges: tuple[int, int, int]
    semigroup: NumericalSemigroup
    orientation_lift_factor: int = 2

    @property
    def conductor(self) -> int:
        """Return the semigroup conductor used by the module floor."""

        return self.semigroup.conductor()

    @property
    def frobenius_number(self) -> int | None:
        """Return the semigroup Frobenius gap."""

        return self.semigroup.frobenius_number()

    @property
    def down_right_charges(self) -> tuple[int, int, int]:
        """Return right charges from the down conductor floor."""

        return subtract_conductor_floor(self.left_charges, self.conductor)

    @property
    def down_diagonal_exponents(self) -> tuple[int, int, int]:
        """Return diagonal down exponents."""

        return diagonal_exponents(self.left_charges, self.down_right_charges)

    @property
    def up_right_charges(self) -> tuple[int, int, int]:
        """Return right charges from the oriented up lift."""

        return oriented_lift_up_right_charges(
            self.left_charges,
            self.down_right_charges,
            lift_factor=self.orientation_lift_factor,
        )

    @property
    def up_diagonal_exponents(self) -> tuple[int, int, int]:
        """Return diagonal up exponents."""

        return diagonal_exponents(self.left_charges, self.up_right_charges)

    def conductor_diagonal_controls(
        self, candidate_conductors: tuple[int, ...] = (1, 2, 3)
    ) -> dict[int, tuple[int, int, int]]:
        """Return down diagonal exponents for nearby conductor controls."""

        return {
            conductor: diagonal_exponents(
                self.left_charges, subtract_conductor_floor(self.left_charges, conductor)
            )
            for conductor in candidate_conductors
        }

    def orientation_lift_controls(
        self, lift_factors: tuple[int, ...] = (1, 2, 3)
    ) -> dict[int, tuple[int, int, int]]:
        """Return up diagonal exponents for nearby lift controls."""

        return {
            lift_factor: diagonal_exponents(
                self.left_charges,
                oriented_lift_up_right_charges(
                    self.left_charges,
                    self.down_right_charges,
                    lift_factor=lift_factor,
                ),
            )
            for lift_factor in lift_factors
        }

    def selected_targets_pass(
        self,
        up_diagonal_target: tuple[int, int, int],
        down_diagonal_target: tuple[int, int, int],
    ) -> bool:
        """Return whether the module rule selects the target diagonals."""

        return (
            self.conductor == 2
            and self.frobenius_number == 1
            and self.down_diagonal_exponents == down_diagonal_target
            and self.up_diagonal_exponents == up_diagonal_target
            and self.conductor_diagonal_controls()[1] != down_diagonal_target
            and self.conductor_diagonal_controls()[2] == down_diagonal_target
            and self.conductor_diagonal_controls()[3] != down_diagonal_target
            and self.orientation_lift_controls()[1] != up_diagonal_target
            and self.orientation_lift_controls()[2] == up_diagonal_target
            and self.orientation_lift_controls()[3] != up_diagonal_target
        )


@dataclass(frozen=True)
class CuspRightChargeOriginAudit:
    """Finite origin audit for the Target-C right-handed charges.

    This audit starts from the left cusp valuations, the cusp conductor, and the
    weak/BCC primitive closure order.  It does not use fitted mass data.  The
    down module is the conductor-ideal residue ``max(q-c,0)``.  The up module is
    the oriented weak-double cover of that down module, so the lift factor is
    the weak/BCC return order rather than an adjustable exponent multiplier.
    """

    left_charges: tuple[int, int, int]
    semigroup: NumericalSemigroup = field(default_factory=lambda: NumericalSemigroup((2, 3)))
    material_source: CuspBoundaryMaterialSource = field(
        default_factory=CuspBoundaryMaterialSource
    )

    @property
    def conductor(self) -> int:
        """Return the cusp conductor."""

        return self.semigroup.conductor()

    @property
    def weak_lift_factor(self) -> int:
        """Return the oriented lift factor from the weak/BCC closure order."""

        return self.material_source.weak_source.primitive_closure_length

    @property
    def color_lift_control_factor(self) -> int:
        """Return the color-center lift control factor."""

        return self.material_source.color_source.primitive_closure_length

    @property
    def down_origin_rule(self) -> str:
        """Return the down-module origin rule."""

        return "right residue of cusp conductor ideal: r_d=max(q-c,0)"

    @property
    def up_origin_rule(self) -> str:
        """Return the up-module origin rule."""

        return "H_tilde oriented weak-double cover: diag_u=N_w diag_d"

    @property
    def down_right_charges(self) -> tuple[int, int, int]:
        """Return down right charges from the conductor quotient."""

        return subtract_conductor_floor(self.left_charges, self.conductor)

    @property
    def down_diagonal_exponents(self) -> tuple[int, int, int]:
        """Return down diagonal exponents from the conductor quotient."""

        return diagonal_exponents(self.left_charges, self.down_right_charges)

    @property
    def up_right_charges(self) -> tuple[int, int, int]:
        """Return up right charges from the weak-order oriented lift."""

        return oriented_lift_up_right_charges(
            self.left_charges,
            self.down_right_charges,
            lift_factor=self.weak_lift_factor,
        )

    @property
    def up_diagonal_exponents(self) -> tuple[int, int, int]:
        """Return up diagonal exponents from the weak-order oriented lift."""

        return diagonal_exponents(self.left_charges, self.up_right_charges)

    @property
    def trivial_lift_control(self) -> tuple[int, int, int]:
        """Return diagonal exponents for no oriented up lift."""

        return diagonal_exponents(
            self.left_charges,
            oriented_lift_up_right_charges(
                self.left_charges, self.down_right_charges, lift_factor=1
            ),
        )

    @property
    def color_lift_control(self) -> tuple[int, int, int]:
        """Return diagonal exponents for the color-order lift control."""

        return diagonal_exponents(
            self.left_charges,
            oriented_lift_up_right_charges(
                self.left_charges,
                self.down_right_charges,
                lift_factor=self.color_lift_control_factor,
            ),
        )

    @property
    def all_right_charges_nonnegative(self) -> bool:
        """Return whether all selected right charges are nonnegative."""

        return all(charge >= 0 for charge in self.down_right_charges + self.up_right_charges)

    @property
    def uses_mass_fits(self) -> bool:
        """Return whether this audit imports fitted mass data."""

        return False

    @property
    def right_charge_origin_pass(self) -> bool:
        """Return whether the origin audit selects the Target-C charges."""

        return (
            self.conductor == 2
            and self.weak_lift_factor == 2
            and self.color_lift_control_factor == 3
            and self.down_right_charges == (1, 0, 0)
            and self.up_right_charges == (5, 2, 0)
            and self.down_diagonal_exponents == (4, 2, 0)
            and self.up_diagonal_exponents == (8, 4, 0)
            and self.trivial_lift_control != self.up_diagonal_exponents
            and self.color_lift_control != self.up_diagonal_exponents
            and self.all_right_charges_nonnegative
            and not self.uses_mass_fits
        )


