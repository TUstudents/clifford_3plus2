@dataclass(frozen=True)
class QLocalLockingOriginAudit:
    """Finite origin audit for the q-mismatch locking penalty.

    The assumptions are deliberately explicit and local: the boundary material
    penalty is a lowest-degree q-local polynomial, invariant under
    ``q -> -q``, non-negative for the adjacent leakage sectors, and vanishes on
    the q=0 scar.  Those constraints force ``p(q)=g q^2`` up to the positive
    stiffness scale ``g``.
    """

    stiffness: sp.Symbol = field(
        default_factory=lambda: sp.symbols("g", positive=True)
    )
    q_values: tuple[int, int, int] = (0, -2, 2)

    @property
    def selected_penalty_values(self) -> tuple[sp.Expr, sp.Expr, sp.Expr]:
        """Return ``g q^2`` evaluated on q=0,+-2."""

        return tuple(sp.simplify(self.stiffness * q_value**2) for q_value in self.q_values)

    @property
    def selected_gap(self) -> sp.Expr:
        """Return the adjacent mixed-normal leakage gap."""

        return sp.simplify(4 * self.stiffness)

    @property
    def visible_penalty_zero(self) -> bool:
        """Return whether the q=0 scar is ungapped."""

        return self.selected_penalty_values[0] == 0

    @property
    def mixed_penalties_degenerate(self) -> bool:
        """Return whether q=+2 and q=-2 have the same gap."""

        return sp.simplify(
            self.selected_penalty_values[1] - self.selected_penalty_values[2]
        ) == 0

    @property
    def mixed_penalty_positive(self) -> bool:
        """Return whether the leakage gap is positive for positive stiffness."""

        return self.selected_gap.is_positive is True

    @property
    def uniqueness_solution(self) -> dict[sp.Symbol, sp.Expr]:
        """Solve the lowest-degree polynomial constraints exactly."""

        q = sp.symbols("q")
        a, b, c = sp.symbols("a b c")
        polynomial = a * q**2 + b * q + c
        solution = sp.solve(
            (
                sp.Eq(polynomial.subs(q, 0), 0),
                sp.Eq(polynomial.subs(q, 2), polynomial.subs(q, -2)),
                sp.Eq(polynomial.subs(q, 2), 4 * self.stiffness),
            ),
            (a, b, c),
            dict=True,
        )
        if len(solution) != 1:
            raise ValueError("q-locking constraints did not select a unique polynomial")
        return solution[0]

    @property
    def selected_polynomial(self) -> sp.Expr:
        """Return the selected polynomial in a fresh variable q."""

        q = sp.symbols("q")
        solution = self.uniqueness_solution
        a, b, c = sp.symbols("a b c")
        return sp.simplify((a * q**2 + b * q + c).subs(solution))

    @property
    def linear_control_values(self) -> tuple[int, int, int]:
        """Return the odd linear-control penalties on q=0,+-2."""

        return self.q_values

