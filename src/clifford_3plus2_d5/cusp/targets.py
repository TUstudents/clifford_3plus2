def exponent_matrix(
    left_charges: tuple[int, int, int], right_charges: tuple[int, int, int]
) -> tuple[tuple[int, int, int], ...]:
    """Return FN exponent matrix ``q_i+r_j``."""

    return tuple(tuple(left + right for right in right_charges) for left in left_charges)


def solve_right_charges_from_diagonal_targets(
    left_charges: tuple[int, int, int],
    diagonal_targets: tuple[int, int, int],
    max_charge: int = 8,
) -> tuple[tuple[int, int, int], ...]:
    """Solve ``left_i + right_i = target_i`` for nonnegative right charges."""

    solutions: list[tuple[int, int, int]] = []
    for first in range(max_charge + 1):
        for second in range(max_charge + 1):
            for third in range(max_charge + 1):
                candidate = (first, second, third)
                diagonal = tuple(left + right for left, right in zip(left_charges, candidate))
                if diagonal == diagonal_targets:
                    solutions.append(candidate)
    return tuple(solutions)


def subtract_conductor_floor(
    charges: tuple[int, int, int], conductor: int
) -> tuple[int, int, int]:
    """Return ``max(q_i-conductor,0)`` for a cusp-conductor floor."""

    return tuple(max(charge - conductor, 0) for charge in charges)


def oriented_double_up_right_charges(
    left_charges: tuple[int, int, int],
    down_right_charges: tuple[int, int, int],
) -> tuple[int, int, int]:
    """Return up right charges from doubled down diagonal exponents."""

    return oriented_lift_up_right_charges(
        left_charges, down_right_charges, lift_factor=2
    )


def oriented_lift_up_right_charges(
    left_charges: tuple[int, int, int],
    down_right_charges: tuple[int, int, int],
    lift_factor: int,
) -> tuple[int, int, int]:
    """Return up right charges from an oriented lift of the down diagonal."""

    down_diagonal = tuple(
        left + right for left, right in zip(left_charges, down_right_charges)
    )
    up_diagonal = tuple(lift_factor * exponent for exponent in down_diagonal)
    return tuple(up - left for up, left in zip(up_diagonal, left_charges))


def diagonal_exponents(
    left_charges: tuple[int, int, int], right_charges: tuple[int, int, int]
) -> tuple[int, int, int]:
    """Return diagonal FN exponents ``q_i+r_i``."""

    return tuple(left + right for left, right in zip(left_charges, right_charges))


