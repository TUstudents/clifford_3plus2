@dataclass(frozen=True)
class RecirculationShearMatcher:
    """Normalized amplitude matcher for weak/color recirculation channels."""

    weak_channels: int
    color_channels: int

    def __post_init__(self) -> None:
        """Validate positive channel counts."""

        if self.weak_channels <= 0 or self.color_channels <= 0:
            raise ValueError("channel counts must be positive")

    @property
    def weak_amplitude(self) -> sp.Expr:
        """Return coherent weak-sector return amplitude."""

        return sp.sqrt(self.weak_channels)

    @property
    def color_amplitude(self) -> sp.Expr:
        """Return coherent color-sector return amplitude."""

        return sp.sqrt(self.color_channels)

    def matching_residual(self, shear: sp.Expr) -> sp.Expr:
        """Return residual of ``(1+t) sqrt(N_w)=sqrt(N_c)``."""

        return sp.simplify((1 + shear) * self.weak_amplitude - self.color_amplitude)

    def matching_function(self, shear: sp.Expr) -> sp.Expr:
        """Return squared norm-matching residual."""

        residual = self.matching_residual(shear)
        return sp.simplify(residual**2)

    def matching_function_derivative(self, shear: sp.Expr) -> sp.Expr:
        """Return derivative of the squared mismatch functional."""

        variable = sp.symbols("t")
        function = self.matching_function(variable)
        return sp.simplify(sp.diff(function, variable).subs(variable, shear))

    def matching_function_second_derivative(self) -> sp.Expr:
        """Return second derivative of the squared mismatch functional."""

        variable = sp.symbols("t")
        function = self.matching_function(variable)
        return sp.simplify(sp.diff(function, variable, 2))

    def solve(self) -> tuple[sp.Expr, ...]:
        """Solve the normalized amplitude-matching equation."""

        shear = sp.symbols("t")
        equation = sp.Eq((1 + shear) * self.weak_amplitude, self.color_amplitude)
        return tuple(sp.simplify(solution) for solution in sp.solve(equation, shear))


def solve_fractional_shear(weak_channels: int, color_channels: int) -> tuple[sp.Expr, ...]:
    """Solve ``(1+t) sqrt(N_w) = sqrt(N_c)`` for the shear ``t``."""

    return RecirculationShearMatcher(weak_channels, color_channels).solve()


@dataclass(frozen=True)
class CuspShearMatchingPrincipleAudit:
    """Finite audit for the CUSP Target-B matching principle.

    The recirculation cusp uses a one-sided outgoing readout: the weak/BCC
    primitive return is locally sheared by the cusp uniformizer ``t`` until its
    coherent amplitude matches the color-center primitive return.  The
    ordinary reflection coefficient is the control for a two-sided scattering
    boundary; it solves a different equation and is therefore excluded by the
    retarded recirculation readout.
    """

    matcher: RecirculationShearMatcher

    @property
    def boundary_matching_condition(self) -> str:
        """Return the physical matching condition being audited."""

        return (
            "one-sided retarded recirculation: (1+t)sqrt(N_w)=sqrt(N_c); "
            "only the weak/BCC branch is locally sheared"
        )

    @property
    def lambda_rec(self) -> sp.Expr:
        """Return the selected cusp uniformizer."""

        solution = self.matcher.solve()
        if len(solution) != 1:
            raise ValueError("expected a unique one-sided shear solution")
        return solution[0]

    @property
    def ordinary_reflection(self) -> sp.Expr:
        """Return the ordinary two-sided reflection coefficient."""

        weak = self.matcher.weak_amplitude
        color = self.matcher.color_amplitude
        return sp.simplify((color - weak) / (color + weak))

    def two_sided_reflection_residual(self, shear: sp.Expr) -> sp.Expr:
        """Return residual of the two-sided reflection matching equation."""

        weak = self.matcher.weak_amplitude
        color = self.matcher.color_amplitude
        return sp.simplify((1 + shear) * weak - (1 - shear) * color)

    @property
    def one_sided_matching_residual_at_lambda(self) -> sp.Expr:
        """Return one-sided residual at the cusp uniformizer."""

        return self.matcher.matching_residual(self.lambda_rec)

    @property
    def ordinary_reflection_two_sided_residual(self) -> sp.Expr:
        """Return two-sided residual at the ordinary reflection coefficient."""

        return self.two_sided_reflection_residual(self.ordinary_reflection)

    @property
    def ordinary_reflection_one_sided_residual(self) -> sp.Expr:
        """Return one-sided recirculation residual at ordinary reflection."""

        return self.matcher.matching_residual(self.ordinary_reflection)

    @property
    def ordinary_reflection_solves_two_sided_control(self) -> bool:
        """Return whether the reflection coefficient solves its control problem."""

        return self.ordinary_reflection_two_sided_residual == 0

    @property
    def ordinary_reflection_excluded_by_one_sided_boundary(self) -> bool:
        """Return whether retarded recirculation rejects ordinary reflection."""

        return sp.simplify(self.ordinary_reflection_one_sided_residual) != 0

    @property
    def positive_oriented_uniformizer_pass(self) -> bool:
        """Return whether the selected uniformizer lies on the small positive branch."""

        return 0 < float(sp.N(self.lambda_rec, 20)) < 1

    @property
    def uses_ckm_data(self) -> bool:
        """Return whether the audit imports CKM data."""

        return False

    @property
    def shear_matching_theorem_pass(self) -> bool:
        """Return whether Target B is promoted to the finite matching theorem."""

        return (
            self.one_sided_matching_residual_at_lambda == 0
            and self.ordinary_reflection_solves_two_sided_control
            and self.ordinary_reflection_excluded_by_one_sided_boundary
            and self.positive_oriented_uniformizer_pass
            and not self.uses_ckm_data
        )


