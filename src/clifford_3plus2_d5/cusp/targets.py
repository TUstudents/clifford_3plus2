@dataclass(frozen=True)
class CenterClosureSource:
    """Minimal center-neutral closure source for a fundamental channel."""

    name: str
    center_order: int
    channel_count: int

    def __post_init__(self) -> None:
        """Validate center data."""

        if self.center_order <= 1:
            raise ValueError("center order must be greater than one")
        if self.channel_count <= 0:
            raise ValueError("channel count must be positive")

    def center_charge(self, tensor_power: int) -> int:
        """Return center charge of a ``tensor_power`` fundamental product."""

        if tensor_power < 0:
            raise ValueError("tensor power must be non-negative")
        return tensor_power % self.center_order

    def is_center_neutral(self, tensor_power: int) -> bool:
        """Return whether a fundamental product is center neutral."""

        return self.center_charge(tensor_power) == 0

    @property
    def one_step_forbidden(self) -> bool:
        """Return whether a one-fundamental return is center charged."""

        return not self.is_center_neutral(1)

    @property
    def primitive_closure_length(self) -> int:
        """Return the first positive center-neutral product length."""

        for tensor_power in range(1, self.center_order + 1):
            if self.is_center_neutral(tensor_power):
                return tensor_power
        raise RuntimeError("unreachable for finite cyclic center")


@dataclass(frozen=True)
class CuspBoundaryMaterialSource:
    """Finite boundary-material source for the cusp recirculation graph.

    The weak/BCC source is the same-normal BCC parity clock: the q=0 edge has
    two same-normal branches and one local tick carries nontrivial parity, so a
    neutral visible return first occurs at order two.  The color source is the
    fundamental SU(3) center clock, whose neutral return first occurs at order
    three.
    """

    q_clock: BCCEdgeQClock = field(default_factory=BCCEdgeQClock)
    color_channel_count: int = 3
    color_center_order: int = 3

    @property
    def weak_source(self) -> CenterClosureSource:
        """Return the weak/BCC source derived from q=0 same-normal parity."""

        return CenterClosureSource(
            "weak_SU2_doublet",
            center_order=self.q_clock.same_normal_count,
            channel_count=self.q_clock.same_normal_count,
        )

    @property
    def color_source(self) -> CenterClosureSource:
        """Return the color-center source."""

        return CenterClosureSource(
            "color_SU3_triplet",
            center_order=self.color_center_order,
            channel_count=self.color_channel_count,
        )

    @property
    def closure_sources(self) -> tuple[CenterClosureSource, CenterClosureSource]:
        """Return the weak/color closure sources selected by the material model."""

        return self.weak_source, self.color_source

    @property
    def same_normal_norm(self) -> sp.Matrix:
        """Return the exact BB q=0 same-normal norm."""

        return exact_bb_same_normal_norm()

    @property
    def mixed_normal_norm(self) -> sp.Matrix:
        """Return the exact BB q-changing leakage norm."""

        return exact_bb_mixed_normal_norm()

    @property
    def total_norm_is_identity(self) -> bool:
        """Return whether same-normal plus leakage blocks complete unitarity."""

        return sp.simplify(
            self.same_normal_norm + self.mixed_normal_norm - sp.eye(2)
        ) == sp.zeros(2)

    @property
    def norm_split_pass(self) -> bool:
        """Return whether the exact BB q=0/leakage split is half plus half."""

        return (
            self.same_normal_norm == sp.eye(2) / 2
            and self.mixed_normal_norm == sp.eye(2) / 2
            and self.total_norm_is_identity
        )

    @property
    def weak_source_from_bcc_pass(self) -> bool:
        """Return whether the weak source is fixed by BCC same-normal parity."""

        return (
            self.q_clock.q_clock_pass
            and self.weak_source.center_order == 2
            and self.weak_source.channel_count == self.q_clock.same_normal_count
            and self.weak_source.primitive_closure_length == 2
            and self.weak_source.one_step_forbidden
        )

    @property
    def color_source_pass(self) -> bool:
        """Return whether the color source is the SU(3) center clock."""

        return (
            self.color_source.center_order == 3
            and self.color_source.channel_count == 3
            and self.color_source.primitive_closure_length == 3
            and self.color_source.one_step_forbidden
        )

    @property
    def material_source_pass(self) -> bool:
        """Return whether the finite material source selects the cusp inputs."""

        return (
            self.norm_split_pass
            and self.weak_source_from_bcc_pass
            and self.color_source_pass
            and tuple(source.primitive_closure_length for source in self.closure_sources)
            == (2, 3)
        )


def cusp_boundary_material_source() -> CuspBoundaryMaterialSource:
    """Return the finite boundary-material source for the cusp graph."""

    return CuspBoundaryMaterialSource()


def matrix_equal(left: sp.Matrix, right: sp.Matrix) -> bool:
    """Return exact equality after entrywise simplification."""

    return left.shape == right.shape and sp.simplify(left - right) == sp.zeros(*left.shape)


def local_bb_boundary_isometry() -> sp.Matrix:
    """Return the stacked local BB boundary isometry.

    The two same-normal blocks and two mixed-normal blocks are the complete
    one-step outgoing channels from the local q=0 spinor input.
    """

    blocks = exact_bb_edge_blocks()
    return sp.Matrix.vstack(
        blocks.b_plus,
        blocks.b_minus,
        blocks.m_plus2,
        blocks.m_minus2,
    ).applyfunc(sp.simplify)


@dataclass(frozen=True)
class CuspBoundaryDilationAudit:
    """Finite local dilation audit for the cusp boundary source.

    This proves that the local BB same/mixed channel stack is an isometry and
    can therefore be completed to a unitary on a finite auxiliary output space.
    It also imports the q-mismatch/outgoing leakage checks used elsewhere in
    the BCC boundary program.  The locking field and outgoing condition remain
    declared material inputs.
    """

    material_source: CuspBoundaryMaterialSource = field(
        default_factory=CuspBoundaryMaterialSource
    )
    remaining_declared_inputs: tuple[str, ...] = (
        "single_clock_locking_field_is_realized_by_boundary_material",
        "mixed_normal_clock_error_ports_are_outgoing_asymptotic_leads",
    )

    @property
    def local_isometry(self) -> sp.Matrix:
        """Return the stacked BB same/mixed outgoing-channel map."""

        return local_bb_boundary_isometry()

    @property
    def local_isometry_norm(self) -> sp.Matrix:
        """Return ``V^dagger V`` for the local outgoing-channel map."""

        isometry = self.local_isometry
        return (isometry.H * isometry).applyfunc(sp.simplify)

    @property
    def local_isometry_pass(self) -> bool:
        """Return whether the local BB channel stack is an isometry."""

        return matrix_equal(self.local_isometry_norm, sp.eye(2))

    @property
    def local_input_dimension(self) -> int:
        """Return the q=0 local spinor input dimension."""

        return self.local_isometry.cols

    @property
    def local_output_dimension(self) -> int:
        """Return the outgoing channel dimension."""

        return self.local_isometry.rows

    @property
    def unitary_completion_nullity(self) -> int:
        """Return the nullity available to complete the isometry to a unitary."""

        return self.local_output_dimension - self.local_isometry.H.rank()

    @property
    def unitary_dilation_exists_pass(self) -> bool:
        """Return whether finite-dimensional unitary completion is certified."""

        return (
            self.local_isometry_pass
            and self.local_output_dimension >= self.local_input_dimension
            and self.unitary_completion_nullity
            == self.local_output_dimension - self.local_input_dimension
        )

    @property
    def finite_gap_feedback_scalar(self) -> bool:
        """Return whether finite q-mismatch feedback is scalar in spinor space."""

        return finite_gap_feedback_is_scalar()

    @property
    def hard_gap_feedback_zero(self) -> bool:
        """Return whether q-changing feedback vanishes in the hard-gap limit."""

        return hard_gap_feedback_is_zero()

    @property
    def retarded_feedback_limit_zero(self) -> bool:
        """Return whether outgoing leakage has zero high-threshold feedback."""

        return retarded_feedback_limit_is_zero()

    @property
    def retarded_visible_powers_match_survival(self) -> bool:
        """Return whether retarded closure preserves q=0 visible powers."""

        return retarded_visible_powers_match_survival()

    @property
    def recurrent_wedge_return(self) -> sp.Matrix:
        """Return the local two-step recurrent leakage return control."""

        return recurrent_two_step_return()

    @property
    def recurrent_wedge_return_nonzero(self) -> bool:
        """Return whether recurrent leakage would feed back into the visible sheet."""

        return not matrix_equal(self.recurrent_wedge_return, sp.zeros(2))

    @property
    def recurrent_visible_powers_differ(self) -> bool:
        """Return whether recurrent closure changes visible powers."""

        return recurrent_visible_powers_differ()

    @property
    def outgoing_leakage_pass(self) -> bool:
        """Return whether leakage is controlled by hard-gap/outgoing conditions."""

        return (
            self.finite_gap_feedback_scalar
            and self.hard_gap_feedback_zero
            and self.retarded_feedback_limit_zero
            and self.retarded_visible_powers_match_survival
            and self.recurrent_wedge_return_nonzero
            and self.recurrent_visible_powers_differ
        )

    @property
    def boundary_dilation_pass(self) -> bool:
        """Return whether the finite boundary dilation audit passes."""

        return (
            self.material_source.material_source_pass
            and self.unitary_dilation_exists_pass
            and self.outgoing_leakage_pass
        )


