def fn_yukawa_matrix(
    left_charges: tuple[int, int, int],
    right_charges: tuple[int, int, int],
    coefficient_matrix: sp.Matrix,
    lam: sp.Expr,
) -> sp.Matrix:
    """Return an FN matrix from a coefficient matrix."""

    return sp.Matrix(
        [
            [
                coefficient_matrix[row, col] * lam ** (left_charges[row] + right_charges[col])
                for col in range(3)
            ]
            for row in range(3)
        ]
    )


def jarlskog_commutator_invariant(yu: sp.Matrix, yd: sp.Matrix) -> sp.Expr:
    """Return ``Im tr([YuYu^dagger,YdYd^dagger]^3)``."""

    hu = yu * yu.conjugate().T
    hd = yd * yd.conjugate().T
    commutator = hu * hd - hd * hu
    return sp.simplify(sp.im(sp.trace(commutator**3)))


def common_left_rephase(
    yu: sp.Matrix, yd: sp.Matrix, phase_exponents: tuple[int, int, int]
) -> tuple[sp.Matrix, sp.Matrix]:
    """Apply one common left-handed center rephasing to both Yukawas."""

    omega = exact_omega()
    rephasing = sp.diag(*(omega**phase for phase in phase_exponents))
    return rephasing * yu, rephasing * yd


def field_rephase_yukawas(
    yu: sp.Matrix,
    yd: sp.Matrix,
    left_phase_exponents: tuple[int, int, int],
    up_right_phase_exponents: tuple[int, int, int],
    down_right_phase_exponents: tuple[int, int, int],
) -> tuple[sp.Matrix, sp.Matrix]:
    """Apply independent center rephasings to all quark field slots.

    The left rephasing is common to the up and down Yukawas because both use
    the same ``Q_L`` fields.  The right rephasings are independent for
    ``u_R`` and ``d_R``.  The commutator invariant must be unchanged.
    """

    omega = exact_omega()
    left = sp.diag(*(omega**phase for phase in left_phase_exponents))
    up_right = sp.diag(*(omega**phase for phase in up_right_phase_exponents))
    down_right = sp.diag(*(omega**phase for phase in down_right_phase_exponents))
    return left * yu * up_right, left * yd * down_right


