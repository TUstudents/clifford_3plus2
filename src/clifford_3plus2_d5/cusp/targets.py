@dataclass(frozen=True)
class RecirculationShearMatcher:
    """Normalized amplitude matcher for weak/color recirculation channels."""

    weak_channels: int
    color_channels: int

    def __post_init__(self) -> None:
        """Validate positive channel counts."""

        if self.weak_channels <= 0 or self.color_channels <= 0:
            raise ValueError("channel counts must be positive")

    @property
    def weak_amplitude(self) -> sp.Expr:
        """Return coherent weak-sector return amplitude."""

        return sp.sqrt(self.weak_channels)

    @property
    def color_amplitude(self) -> sp.Expr:
        """Return coherent color-sector return amplitude."""

        return sp.sqrt(self.color_channels)

    def matching_residual(self, shear: sp.Expr) -> sp.Expr:
        """Return residual of ``(1+t) sqrt(N_w)=sqrt(N_c)``."""

        return sp.simplify((1 + shear) * self.weak_amplitude - self.color_amplitude)

    def matching_function(self, shear: sp.Expr) -> sp.Expr:
        """Return squared norm-matching residual."""

        residual = self.matching_residual(shear)
        return sp.simplify(residual**2)

    def matching_function_derivative(self, shear: sp.Expr) -> sp.Expr:
        """Return derivative of the squared mismatch functional."""

        variable = sp.symbols("t")
        function = self.matching_function(variable)
        return sp.simplify(sp.diff(function, variable).subs(variable, shear))

    def matching_function_second_derivative(self) -> sp.Expr:
        """Return second derivative of the squared mismatch functional."""

        variable = sp.symbols("t")
        function = self.matching_function(variable)
        return sp.simplify(sp.diff(function, variable, 2))

    def solve(self) -> tuple[sp.Expr, ...]:
        """Solve the normalized amplitude-matching equation."""

        shear = sp.symbols("t")
        equation = sp.Eq((1 + shear) * self.weak_amplitude, self.color_amplitude)
        return tuple(sp.simplify(solution) for solution in sp.solve(equation, shear))


def solve_fractional_shear(weak_channels: int, color_channels: int) -> tuple[sp.Expr, ...]:
    """Solve ``(1+t) sqrt(N_w) = sqrt(N_c)`` for the shear ``t``."""

    return RecirculationShearMatcher(weak_channels, color_channels).solve()


