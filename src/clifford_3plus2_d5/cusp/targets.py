@dataclass(frozen=True)
class CenterHolonomyPath:
    """One finite coefficient path with an SU(3)-center holonomy."""

    row: int
    col: int
    amplitude: sp.Expr
    center_power: int

    def __post_init__(self) -> None:
        """Validate matrix location."""

        if self.row not in (0, 1, 2) or self.col not in (0, 1, 2):
            raise ValueError("path row/col must be 0, 1, or 2")

    @property
    def normalized_center_power(self) -> int:
        """Return center power modulo three."""

        return self.center_power % 3

    def contribution(self) -> sp.Expr:
        """Return the complex path contribution."""

        return sp.simplify(self.amplitude * exact_omega() ** self.normalized_center_power)


