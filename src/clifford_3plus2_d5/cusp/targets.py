def walk_path_edges(prefix: str, length: int) -> tuple[tuple[str, str], ...]:
    """Return edges for one visible-rooted closed path of fixed length."""

    if length <= 0:
        raise ValueError("length must be positive")
    if length == 1:
        return (("visible", "visible"),)
    nodes = tuple(f"{prefix}_{index}" for index in range(1, length))
    edges = [("visible", nodes[0])]
    edges.extend((nodes[index], nodes[index + 1]) for index in range(len(nodes) - 1))
    edges.append((nodes[-1], "visible"))
    return tuple(edges)


