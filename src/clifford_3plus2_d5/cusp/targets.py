def cusp_module_path_count(value: int, semigroup: NumericalSemigroup) -> int:
    """Return the finite module path count for one FN exponent.

    Semigroup elements are counted by their decompositions into primitive
    `(2,3)` cusp returns.  The gap/contact exponents are kept as one irreducible
    boundary-module path instead of being set to zero.
    """

    if value < 0:
        raise ValueError("FN exponents must be nonnegative")
    return max(1, len(semigroup.decompositions(value)))


